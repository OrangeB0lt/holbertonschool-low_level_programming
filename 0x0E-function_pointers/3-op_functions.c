#include <stdio.h>

/**
 * op_add - adds
 * @a: num 1
 * @b: num 2
 * Return: returns sum
 **/

int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - subtracts
 * @a: num 1
 * @b: num 2
 * Return: difference
 **/

int op_sub(int a, int b)
{
	return ((a) - (b));
}

/**
 * op_mul - mutiplies
 * @a: num 1
 * @b: num 2
 * Return: product
 **/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: num 1
 * @b: num 2
 * Return: divisor
 **/

int op_div(int a, int b)
{
	return ((a) / (b));
}

/**
 * op_mod - modulus
 * @a: num 1
 * @b: num 2
 * Return: remainder
 **/

int op_mod(int a, int b)
{
	return ((a) % (b));
}
